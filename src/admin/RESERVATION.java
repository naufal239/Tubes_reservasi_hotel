    public void tampildataivan() {
    model = new DefaultTableModel();
    this.jTable1.setModel(model);
    model.addColumn("id_transaksi");
    model.addColumn("nama");
    model.addColumn("no_ktp");
    model.addColumn("no_hp");
    model.addColumn("Tgl_masuk");
    model.addColumn("Tgl_keluar");
    model.addColumn("tempat");
    model.addColumn("harga");
    model.addColumn("status");

    String sql = "SELECT * FROM reservation";

    try (Connection conn = Db.getConnection();
         PreparedStatement pstmt = conn.prepareStatement(sql);
         ResultSet rs = pstmt.executeQuery()) {

        while (rs.next()) {
            // Ambil data sesuai kolom pada database
            String idreservasi = rs.getString("id_transaksi");
            String nama = rs.getString("nama");
            String noktp = rs.getString("no_ktp");
            String nohp = rs.getString("no_hp");
            String tglMasuk = rs.getString("check_in");
            String tglKeluar = rs.getString("check_out");
            String tempat = rs.getString("tipe");
            String harga = rs.getString("harga");
            String status = rs.getString("status");

            // Masukkan data ke dalam tabel
            model.addRow(new Object[] { idreservasi, nama, noktp, nohp, tglMasuk, tglKeluar, tempat, harga, status });
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "TERJADI KESALAHAN! " + e.getMessage());
    }
}

    
private void klikTabel() {
    // Mendapatkan baris yang dipilih di jTable1
    int i = this.jTable1.getSelectedRow();
    if (i == -1) {
        JOptionPane.showMessageDialog(this, "Silakan pilih baris dalam tabel terlebih dahulu.");
        return;
    }

    // Mengambil data dari model tabel berdasarkan indeks baris
    String id = (String) model.getValueAt(i, 0);
    this.txt_nama.setText(id);

    String nama = (String) model.getValueAt(i, 1);
    this.txt_ktp.setText(nama);

    String tglMasuk = (String) model.getValueAt(i, 2);
    try {
        // Konversi String tanggal menjadi java.util.Date
        SimpleDateFormat dateFormat = new SimpleDateFormat("mm-dd-yy"); // Format sesuai data di tabel
        java.util.Date dateIn = dateFormat.parse(tglMasuk);
        this.chk_in.setDate(dateIn); // Atur tanggal ke JDateChooser
    } catch (ParseException e) {
        JOptionPane.showMessageDialog(this, "Format tanggal check-in tidak valid: " + e.getMessage());
    }

    String tglKeluar = (String) model.getValueAt(i, 3);
    try {
        // Konversi String tanggal menjadi java.util.Date
        SimpleDateFormat dateFormat = new SimpleDateFormat("mm-dd-yy"); // Format sesuai data di tabel
        java.util.Date dateOut = dateFormat.parse(tglKeluar);
        this.chk_out.setDate(dateOut); // Atur tanggal ke JDateChooser
    } catch (ParseException e) {
        JOptionPane.showMessageDialog(this, "Format tanggal check-out tidak valid: " + e.getMessage());
    }

    String tempat = (String) model.getValueAt(i, 4);
    this.txt_tempat.setText(tempat);

    String harga = (String) model.getValueAt(i, 5);
    this.txt_harga.setText(harga);

}

  
