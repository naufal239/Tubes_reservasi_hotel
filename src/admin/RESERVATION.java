    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed

  try (Connection conn = Db.getConnection()) { // Pastikan Db.getConnection() sudah terhubung dengan database
    int ok = JOptionPane.showConfirmDialog(
        null,
        "Apakah yakin untuk Mengubah Data ini?",
        "Confirmation",
        JOptionPane.YES_NO_OPTION
    );

    if (ok == JOptionPane.YES_OPTION) {
        // Query untuk update data
        String sql = "UPDATE reservation SET " +
                     "id_transaksi=?, nama=?, no_ktp=?, check_in=?, check_out=?, " +
                     "tipe=?, harga=?, status=? " +
                     "WHERE id_transaksi=?";
                     
        try (PreparedStatement ps = conn.prepareStatement(sql)) {
            // Mengisi parameter untuk query
            ps.setString(1, txt_id.getText()); // ID Transaksi
            ps.setString(2, txt_nama.getText()); // Nama
            ps.setString(3, txt_ktp.getText()); // Nomor KTP
            ps.setString(4, new SimpleDateFormat("yyyy-MM-dd").format(chk_in.getDate())); // Tanggal check-in
            ps.setString(5, new SimpleDateFormat("yyyy-MM-dd").format(chk_out.getDate())); // Tanggal check-out
            ps.setString(6, txt_tempat.getText()); // Tipe kamar
            ps.setString(7, txt_harga.getText()); // Harga

            // Mengambil nilai dari ComboBox
            String statusValue = status.getSelectedItem().toString();
            ps.setString(8, statusValue);
            ps.setString(9, txt_id.getText()); // ID Transaksi untuk WHERE clause
            
            // Eksekusi update
            ps.executeUpdate();

            // Tampilkan data baru dan kosongkan form input
            tampildataivan();
            ClearForm();
            JOptionPane.showMessageDialog(null, "UPDATE DATA SUKSES");
        }
    }
} catch (SQLException | HeadlessException e) {
    JOptionPane.showMessageDialog(null, "UPDATE DATA GAGAL: " + e.getMessage());
}

    }//GEN-LAST:event_jButton5ActionPerformed

