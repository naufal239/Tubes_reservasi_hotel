    private void BTN_DELETEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTN_DELETEActionPerformed
    int ok = JOptionPane.showConfirmDialog(
        null, 
        "Apakah yakin untuk Menghapus Data ini?", 
        "Confirmation", 
        JOptionPane.YES_NO_OPTION
    );

    if (ok == JOptionPane.YES_OPTION) {
        // Ambil ID_transaksi dari komponen GUI (misalnya, dari teks field atau tabel)
        String idTransaksi = txt_id.getText(); // Pastikan txt_nama berisi ID_transaksi
        if (idTransaksi == null || idTransaksi.isEmpty()) {
            JOptionPane.showMessageDialog(
                null, 
                "Pilih data dari tabel terlebih dahulu!"
            );
            return;
        }

        String sql = "DELETE FROM reservation WHERE id_transaksi = ?";
        try (Connection conn = Db.getConnection();
             PreparedStatement pstmt = conn.prepareStatement(sql)) {

            // Mengatur nilai parameter untuk query DELETE
            pstmt.setString(1, idTransaksi);

            // Eksekusi query DELETE
            int rowsAffected = pstmt.executeUpdate();

            // Periksa apakah ada baris yang dihapus
            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(
                    null, 
                    "Data berhasil dihapus!"
                );

                // Bersihkan form setelah penghapusan
                ClearForm();

                // Muat ulang data di tabel setelah penghapusan
                tampildataivan();
            } else {
                JOptionPane.showMessageDialog(
                    null, 
                    "Data gagal dihapus. Periksa ID_transaksi."
                );
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(
                null, 
                "Terjadi kesalahan: " + e.getMessage()
            );
        }
    }
    }//GEN-LAST:event_BTN_DELETEActionPerformed

